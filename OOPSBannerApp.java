public class OOPSBannerApp {
    public static void main(String[] args) {
        System.out.println(String.join(" ", " *****  ", " *****  ", "****   ", " *****  "));
        System.out.println(String.join(" ", "*     * ", "*     * ", "*   *  ", "*       "));
        System.out.println(String.join(" ", "*     * ", "*     * ", "*   *  ", "*       "));
        System.out.println(String.join(" ", "*     * ", "*     * ", "****   ", " *****  "));
        System.out.println(String.join(" ", "*     * ", "*     * ", "*      ", "      * "));
        System.out.println(String.join(" ", "*     * ", "*     * ", "*      ", "      * "));
        System.out.println(String.join(" ", " *****  ", " *****  ", "*      ", " *****  "));
    }
}